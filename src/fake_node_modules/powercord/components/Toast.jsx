const { React } = require('powercord/webpack');
const { sleep } = require('powercord/util');

module.exports = class Toast extends React.Component {
  constructor (props) {
    super(props);

    this.state = {
      header: props.header,
      buttons: props.buttons,
      content: props.content
    };
  }

  async remove () {
    this.setState({ leaving: true });
    await sleep(500);
    document.getElementById('powercord-updater').remove();
  }

  render () {
    const { props, state } = this;

    return (
      <div
        className={`powercord-toast${state.leaving ? ' leaving' : ''}`}
        style={props.style}
      >
        {state.header && (
          <div className='header'>
            {state.header}
          </div>
        )}

        <div className='body'>
          {state.content && (
            <div className='content'>
              {state.content}
            </div>
          )}

          <div className='buttons'>
            {state.buttons.filter(b => b).map(button => (
              <div className='button' id={button.type} onClick={() => button.onClick(this.remove.bind(this))}>
                {button.text}
              </div>
            ))}
          </div>
        </div>
      </div>
    );
  }
};
