const { getComponentByDisplayName, getModuleByDisplayName, React } = require('powercord/webpack');

const Flex = getModuleByDisplayName('Flex');
const DFormItem = getComponentByDisplayName('FormItem');
const FormDivider = getComponentByDisplayName('FormDivider');
const FormText = getComponentByDisplayName('FormText');
const Button = getComponentByDisplayName('DropdownButton');

const flexClassName = `${Flex.Direction.VERTICAL} ${Flex.Justify.START} ${Flex.Align.STRETCH} ${Flex.Wrap.NO_WRAP}`;

module.exports = class ButtonItem extends React.Component {
  constructor () {
    super();

    const modules = powercord.pluginManager.get('pc-classNameNormalizer')._fetchAllModules();

    this.classMargins = modules.find(m => Object.keys(m).includes('marginTop20'));
    this.classTitle = modules.find(m => Object.keys(m).includes('titleDefault')).titleDefault;
    this.classDescription = modules.find(m => Object.keys(m).includes('formText')).description;
  }

  render () {
    return <DFormItem
      className={`powercord-settings-item powercord-buttonitem ${flexClassName} ${this.classMargins.marginBottom20}`}>
      <div className='powercord-settings-item-title'>
        <div>
          <div className={this.classTitle}>
            {this.props.children}
          </div>
          <FormText className={this.classDescription}>
            {this.props.note}
          </FormText>
        </div>
        <Button onClick={() => this.props.onClick()}>{this.props.button}</Button>
      </div>
      <FormDivider className={this.classMargins.marginTop20}/>
    </DFormItem>;
  }
};
