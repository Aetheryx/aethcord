const { getModuleByDisplayName, React } = require('powercord/webpack');

const Flex = getModuleByDisplayName('Flex');
const DFormItem = getModuleByDisplayName('FormItem');
const FormDivider = getModuleByDisplayName('FormDivider');
const FormText = getModuleByDisplayName('FormText');

const flexClassName = `${Flex.Direction.VERTICAL} ${Flex.Justify.START} ${Flex.Align.STRETCH} ${Flex.Wrap.NO_WRAP}`;

module.exports = class Category extends React.Component {
  constructor () {
    super();

    const modules = powercord.pluginManager.get('pc-classNameNormalizer')._fetchAllModules();

    this.classMargins = modules.find(m => Object.keys(m).includes('marginTop20'));
    this.classTitle = modules.find(m => Object.keys(m).includes('titleDefault')).titleDefault;
    this.classDescription = modules.find(m => Object.keys(m).includes('formText')).description;
  }

  render () {
    return (
      <DFormItem className={`powercord-settings-item powercord-category ${flexClassName} ${this.classMargins.marginBottom20}`}>
        <div className='powercord-settings-item-title' onClick={() => this.props.onChange(!this.props.opened)}>
          <svg xmlns='http://www.w3.org/2000/svg' viewBox='0 0 24 24' className={this.props.opened ? 'opened' : ''}>
            <path
              fill='#fff'
              d='M9.29 15.88L13.17 12 9.29 8.12c-.39-.39-.39-1.02 0-1.41.39-.39 1.02-.39 1.41 0l4.59 4.59c.39.39.39 1.02 0 1.41L10.7 17.3c-.39.39-1.02.39-1.41 0-.38-.39-.39-1.03 0-1.42z'/>
          </svg>
          <div>
            <div className={this.classTitle}>
              {this.props.name}
            </div>
            <FormText className={this.classDescription}>
              {this.props.description}
            </FormText>
          </div>
        </div>
        {this.props.opened
          ? <div className='powercord-settings-item-inner'>
            {this.props.children}
          </div>
          : <FormDivider className={this.classMargins.marginTop20}/>}

      </DFormItem>
    );
  }
};
